			<p>Originating in 2011, Wonderland is an annual music and cultural event, owned and operated by independent WA event creatives Metric, the team behind well-respected WA institutions Castaway, Jack Rabbit Slims, By The C, Father Touring, CIRCO and more. Wonderland has always been a Festival characterised by adventure – as such, during its six-year history, this free-roaming fun-fest has traversed the most dreamy and disparate of territories – from the garden grounds of a heritage art gallery in Fremantle, to an open-air performance space in the Perth Hills surrounded by forest (Belvoir Ampitheatre), to the rooftop of an abandoned department store by the seaside. 2014’s event saw the festival descend upon sprawling inner-city urban oasis The Supreme Court Gardens: over 5,000 WA patrons flocked to the botanic paradise.</p><br>
			<p>Last year’s 2015 event – it’s fifth chapter – saw the team present their largest-scale offering to date… and their most successful. The festival ‘took it to skies’, with a sky/party-kingdom themed event at Langley Park, on the stunning Swan River’s shores, for an unforgettable day that witnessed nearly 12,000 people reveling in the sunshine and sounds of Broods, Alison Wonderland, Hot Dub Time Machine, Dune Rats and more, with the wonderful Perth city skyline making the perfect backdrop for the festival’s stage. Last year also marked the event’s transformation into a fully-fledged cultural affair, with gourmet food, a Silent Disco, bespoke bars, carnival rides, and a bohemian marketplace of goods and services housed in giant Teepee tents – the Village</p><br>
			<p>In December 2016, Wonderland dips into our treasured party tale once more, this time trekking away from the river and the sea and adventuring into the woodlands, with the festival’s 2016 edition taking place in the lush forested heart of Kitchener Park, Subiaco, with a legendary line up of electronic and indie talent, the return of bespoke bar offerings (and a new ‘Beer land’ concept), food trucks, rides, a (double-sized) Teepee Village marketplace, and a focus – led by the festival’s forest location – on green environmental initiatives.</p><br>
